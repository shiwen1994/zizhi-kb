tc2s = make_trans()

def parse(raw):
    # 移除 markup
    text = re.sub('<ref[^>]*>.*?</ref>', '', raw, flags=re.DOTALL)
    text = re.sub('<ref[^>]*/>', '', text)
    text = re.sub(r'\[\[[^\]|]+\|([^\]]+)\]\]', r'\1', text)
    text = re.sub(r'\[\[([^\]|]+)\]\]', r'\1', text)
    text = re.sub(r"'''(.+?)'''", r'\1', text)
    text = re.sub(r"''(.+?)''", r'\1', text)
    text = re.sub(r'{{[^|{}]+}}', '', text)
    text = re.sub(r'{{[^|{}]*\|', '', text)
    text = tc2s(text)
    return text

def extract_entries(text):
    """解析文本，提取条目"""
    entries = []
    # 按王拆分
    king_blocks = re.split(r'\n== (.+?) ==\n', text)
    i = 1
    while i < len(king_blocks) - 1:
        king = king_blocks[i].strip()
        content = king_blocks[i+1] if i+1 < len(king_blocks) else ''
        # 按年份拆分
        year_blocks = re.split(r'\n=== (.+?) ===', content)
        j = 1
        while j < len(year_blocks) - 1:
            year_title = year_blocks[j].strip()
            year_body = year_blocks[j+1] if j+1 < len(year_blocks) else ''
            lines = year_body.split('\n')
            current = None
            buf = []
            for line in lines:
                line = line.strip()
                if not line:
                    continue
                # 条目标题：数字序号开头
                if re.match(r'^[\d十百千万零一二三四五六七八九十壹贰叁肆伍陆柒捌玖拾]+[、，,.\s　]+', line):
                    if current:
                        current['main_text'] = '\n'.join(buf).strip()
                        entries.append(current)
                    # 分离主文和臣光曰
                    if '::' in line:
                        segs = line.split('::')
                        main = segs[0].strip()
                        guang = '::'.join(segs[1:])
                    else:
                        main = line
                        guang = ''
                    current = {'king': king, 'year': year_title, 'main_text': main, 'guangzhu': guang}
                    buf = []
                elif current:
                    if line.startswith('::'):
                        if buf:
                            current['main_text'] = '\n'.join(buf).strip()
                            buf = []
                        current['guangzhu'] = (current.get('guangzhu','') + '\n' + line).strip()
                    else:
                        buf.append(line)
            if current:
                if buf and not current.get('main_text'):
                    current['main_text'] = '\n'.join(buf).strip()
                elif buf and current.get('main_text'):
                    current['main_text'] += '\n' + '\n'.join(buf)
                entries.append(current)
            j += 2
        i += 2
    return entries

